"""쿼리 재작성 프롬프트.

목적: 종목 관련 뉴스 검색을 위해 단일 쿼리를 다양한 관점의 쿼리 2~3개로 확장.
- variant 1: 실적/재무 관련 (매출·영업이익·분기실적)
- variant 2: 기업 이벤트 (M&A, 임원변경, 계약, 규제)
- variant 3: 산업/섹터 (원자재, 경쟁사, 정책 등)

너무 general 하면 recall 이 안 오르고 dupe 됨. 종목 이름/심볼을 반드시 포함하도록 요구.
"""

SYSTEM_PROMPT = (
    "당신은 한국 주식 뉴스 검색을 위한 쿼리 리라이터입니다. "
    "주어진 종목의 최근 주가 변동 원인을 찾기 위해, 서로 다른 관점의 검색 쿼리 2~3개를 생성합니다. "
    "각 쿼리는 반드시 종목명이나 심볼을 포함해야 하며, 서로 관점이 겹치지 않도록 다양화합니다.\n\n"
    "관점 예시:\n"
    "- 실적/재무 (분기 매출, 영업이익, 어닝서프라이즈, 가이던스)\n"
    "- 기업 이벤트 (M&A, 임원 교체, 자사주, 배당, 계약 체결, 규제 이슈)\n"
    "- 산업/섹터 (원자재 가격, 경쟁사 동향, 정부 정책)\n\n"
    "출력 형식은 반드시 JSON 배열 문자열 (예: [\"쿼리1\", \"쿼리2\", \"쿼리3\"]). "
    "그 외 텍스트/마크다운/주석 없이 JSON 만."
)


def build_user_prompt(symbol: str, stock_name: str, base_query: str) -> str:
    return (
        f"종목: {stock_name} ({symbol})\n"
        f"기본 쿼리: {base_query}\n\n"
        "위 종목의 최근 주가 변동 원인을 다양한 관점에서 찾을 수 있도록 "
        "서로 겹치지 않는 검색 쿼리 2~3개를 JSON 배열로 반환하세요."
    )
